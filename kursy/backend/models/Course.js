const mongoose = require('mongoose');

const CourseSchema = new mongoose.Schema({
  title: {
    type: String,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  image: {
    type: String, // URL do obrazka
    required: true,
  },
});

module.exports = mongoose.model('Course', CourseSchema);
